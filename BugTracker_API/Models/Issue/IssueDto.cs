    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A get issue data transfer object. </summary>
    ///
    /// <remarks>   Dawid, 18/06/2020. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class GetIssueDto
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public long Id { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of issue.  </summary>
        ///
        /// <value> The issue number. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int Number { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the title. </summary>
        ///
        /// <value> The title. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Title { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the body. </summary>
        ///
        /// <value> The body. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Body { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the status. </summary>
        ///
        /// <value> The status. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Status { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the created at timestamp. </summary>
        ///
        /// <value> The created at timestamp. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime CreatedAt { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of comments. </summary>
        ///
        /// <value> The number of comments. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int Comments { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the user. </summary>
        ///
        /// <value> The user. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public UserDto User { get; set; }
    }

