using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;

namespace BugTracker_API.Models
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   An issue. </summary>
    ///
    /// <remarks>   Dawid, 18/06/2020. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class Issue
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public long Id { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the number of. </summary>
        ///
        /// <value> The number. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Required]
        public int Number { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the title. </summary>
        ///
        /// <value> The title. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Required]
        public string Title { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the body. </summary>
        ///
        /// <value> The body. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Required]
        public string Body { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the status. </summary>
        ///
        /// <value> The status. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Status { get; set; } = "open";

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the Date/Time of the created at timestamp. </summary>
        ///
        /// <value> The created at timestamp. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime CreatedAt { get; set; } = DateTime.UtcNow;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the comments. </summary>
        ///
        /// <value> The comments. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public List<Comment> Comments { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the project. </summary>
        ///
        /// <value> The project. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Project Project { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the user. </summary>
        ///
        /// <value> The user. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public User User { get; set; }
    }
}
