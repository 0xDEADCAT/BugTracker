using System.Collections.Generic;

namespace BugTracker_API.Models
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   An user. </summary>
    ///
    /// <remarks>   Dawid, 18/06/2020. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class User
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier. </summary>
        ///
        /// <value> The identifier. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public int Id { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the username. </summary>
        ///
        /// <value> The username. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Username { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the password hash. </summary>
        ///
        /// <value> The password hash. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public byte[] PasswordHash { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the password salt. </summary>
        ///
        /// <value> The password salt. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public byte[] PasswordSalt { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the projects. </summary>
        ///
        /// <value> The projects. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public List<Project> Projects { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the issues. </summary>
        ///
        /// <value> The issues. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public List<Issue> Issues { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the comments. </summary>
        ///
        /// <value> The comments. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public List<Comment> Comments { get; set; }
    }
}
